#include <iostream>

int main() {
    std::cout << "Number of bytes in a short: " << sizeof(short) << std::endl;
    std::cout << "Number of bytes in a int: " << sizeof(int) << std::endl;
    std::cout << "Number of bytes in a long: " << sizeof(long) << std::endl;
    std::cout << "Number of bytes in a long long: " << sizeof(long long) << std::endl;
    std::cout << "Number of bytes in a float: " << sizeof(float) << std::endl;
    std::cout << "Number of bytes in a double: " << sizeof(double) << std::endl;

    return 0;
}
